
package bestfriends;

public class BestFriends {

    public static void main(String[] args) {
        String[] friends=new String[4];
        friends[0]="John";
        friends[1]="Nick";
        friends[2]="Chris";
        friends[3]="Jim";

        
        for (String friend : friends) {
            System.out.println("Friend: " + friend);
        }
    
    }
    
}
